class ImportItem(DictItem):
            
    PATH = 'PATH'
    MODE = 'MODE'
    VALUE = 'VALUE'
    STATUS = 'STATUS'
    FIELDS = [ PATH, MODE, VALUE, STATUS ]
    
    PATH_IDX = 0
